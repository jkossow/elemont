    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        Material m = new Material();
               
        MaterialEditWindow d = new MaterialEditWindow(null, true);
        d.setMaterial( m );
        initDictionary(d);
        d.setVisible( true );
        
        if( d.getReturnStatus() == JmEditWindow.RET_OK ) {
            dataModel.create( m );
            
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
                       
        MaterialEditWindow d = new MaterialEditWindow(null, true);
        initDictionary(d);
        d.setMaterial( selectedItem );
        
        d.setVisible( true );
        
        if( d.getReturnStatus() == JmEditWindow.RET_OK ) {
            dataModel.update( selectedItem );
            
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void delButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delButtonActionPerformed
        // TODO add your handling code here:
        dataModel.delete( selectedItem );
    }//GEN-LAST:event_delButtonActionPerformed

    private void initDictionary( MaterialEditWindow d ) {
        
        d.setJm( jmRepo.findAll() );
        d.setGrupy( grupaRepo.findAll() );
        
    }
    
    
